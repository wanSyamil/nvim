local function whitespace_flexible_grep(opts, ctx)
  if ctx.filter.search ~= "" then
    ctx = ctx:clone(opts)
    ctx.filter = ctx.filter:clone()
    ctx.filter.search = ctx.filter.search:gsub("%s+", [[\s*]])
  end

  return require("snacks.picker.source.grep").grep(opts, ctx)
end

